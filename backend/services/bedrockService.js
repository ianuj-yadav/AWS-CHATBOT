const { BedrockRuntimeClient, InvokeModelCommand, ConverseCommand, ConverseStreamCommand } = require("@aws-sdk/client-bedrock-runtime");

// Initialize the Bedrock client
// It will automatically use AWS credentials from the environment variables (AWS_ACCESS_KEY_ID, AWS_SECRET_ACCESS_KEY)
const client = new BedrockRuntimeClient({
  region: process.env.AWS_REGION || "us-east-1",
});

/**
 * Service to generate a response from AWS Bedrock.
 * Fallbacks to a mock response if AWS credentials are not set.
 */
const generateResponse = async (message) => {
  // Check if AWS keys are provided, if not, use mock logic
  if (!process.env.AWS_ACCESS_KEY_ID || !process.env.AWS_SECRET_ACCESS_KEY) {
    return getMockResponse(message);
  }

  try {
    const modelId = "amazon.nova-lite-v1:0";

    const command = new ConverseCommand({
      modelId,
      messages: [{
        role: "user",
        content: [{ text: message }]
      }],
      inferenceConfig: {
        maxTokens: 1000,
        temperature: 0.7
      }
    });

    const response = await client.send(command);
    const answer = response.output?.message?.content?.[0]?.text || "No response generated.";

    return {
      answer,
      sources: [{ name: "Generated by Bedrock", type: "ai" }],
    };
  } catch (error) {
    console.error("Error calling Bedrock:", error);
    // Fallback to mock on error (e.g., invalid credentials)
    return getMockResponse(message);
  }
};

